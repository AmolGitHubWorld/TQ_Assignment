package com.assignment_Looping;

public class Table_of_7_using_while 
{

	public static void main(String[] args) 
	{
		
		int n=7;
		int i=1;
		System.out.println("________Table of 7________");
		
		while(i<=10) 	
		{
			
			System.out.println("Sqr of 7 is :" +(n*i));
			i++;
			
		}
		
	}

}
